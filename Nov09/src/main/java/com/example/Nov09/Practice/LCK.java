package com.example.Nov09.Practice;

public class LCK {
	
	private Integer 킬;
	private Integer 데스;
	private Integer 어시;
	
	/**
	 * 인스턴스를 생성하고, 모든 속성을 초기화하는 메서드
	 * @param 킬
	 * @param 데스
	 * @param 어시
	 */
	public LCK(Integer 킬, Integer 데스, Integer 어시) {
		this.킬 = 킬;
		this.데스 = 데스;
		this.어시 = 어시;
	}
	
	/**
	 * 킬 속성을 반환하는 메서드
	 * @return
	 */
	public Integer 킬() {
		return 킬;
	}

}
